                <h3>Clinico-epidemiologic characteristics of the 2019 dengue outbreak in Bangladesh</h3>
                <p><strong>Background:</strong> Dengue fever shows a broad range of clinical presentations worldwide. Here we report on our clinical findings during the 2019 dengue outbreak in one of the largest tertiary care hospitals in Dhaka, the capital of Bangladesh.</p>
                <p><strong>Methods:</strong> A total of 747 suspected dengue cases (553 confirmed and 194 probable) were interviewed with a pro forma case record form. Statistical analyses were conducted using SPSS 20.0. Ethical clearance was obtained from the Dhaka Medical College.</p>
                <p><strong>Results:</strong> The mean age of the dengue cases was 27 y and approximately two-thirds were male. Positive tests for NS1 and anti-dengue immunoglobulin M antibody were present in 91.9% and 59.4% of the cases, respectively. Thrombocytopenia was present in 69% of cases and fever was present in 99.1% of cases. Gastrointestinal (GI) features, including anorexia and/or vomiting (69.4%), abdominal pain (39.8%) and diarrhoea (25.6%), were more prevalent than typical rash and pain symptoms. Hypotension was present in approximately one-quarter of patients (25.4%). Probable and confirmed dengue cases have shown similar clinical characteristics and laboratory findings.</p>
                <p><strong>Conclusions:</strong> The 2019 outbreak of dengue fever in Bangladesh was characterized by increased presentation with GI features. Recognition of this trend would permit early diagnosis and proper management of patients.</p>
