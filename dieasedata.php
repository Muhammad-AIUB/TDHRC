                <h3>Adherence to inhalers and associated factors among adult asthma patients: an outpatient-based study in a tertiary hospital of Rajshahi, Bangladesh</h3>
                <p><strong>Background:</strong> Adherence to inhaler medication is an important contributor to optimum asthma control along with adequate pharmacotherapy. The objective of the present study was to assess self-reported adherence levels and to identify the potential factors associated with non-adherence to inhalers among asthma patients.</p>
                <p><strong>Methods:</strong> This facility-based cross-sectional study was conducted in the medicine outpatient department of Rajshahi Medical College Hospital from November 2020 to January 2021. A total of 357 clinically confirmed adult asthma patients were interviewed. Inhaler adherence was measured using the 10-item Test of Adherence scale (TAI). Both descriptive and inferential statistics were used to express the socio-demographic characteristics of the patients and predictors of poor adherence to inhalers.</p>
                <p><strong>Results:</strong> A substantial number of participants were non-adherent (86%) to inhaler medication. Non-adherent patients were often younger (23.15, 95% CI 3.67-146.08), lived in rural areas (23.28, 95% CI 2.43-222.66), had fewer years of schooling (5.69, 95% CI 1.27-25.44), and belonged to middle-income households (aOR 9.74, 95% CI 2.11-44.9) compared to adherent patients. The presence of comorbidities (12.91, 95% CI 1.41-117.61), prolonged duration of inhaler use (5.69, 95% CI 1.22-26.49), and consulting non-qualified practitioners (13.09, 95% CI 3.10-55.26) were significant contributors to non-adherence.</p>
                <p><strong>Conclusion:</strong> Despite ongoing motivation and treatment, non-adherence to inhalation anti-asthmatic medication is high, with several contributing factors identified. Regular monitoring and a guided patient-centered self-management approach might be helpful in addressing these factors in the long run.</p>
