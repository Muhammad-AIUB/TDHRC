                <h2>Health system data:</h2>
                <h3>Patient self-referral patterns in a developing country: characteristics, prevalence, and predictors</h3>
                <p style="text-align: justify;"><strong>Background:</strong> Efficient healthcare delivery and access to specialized care rely heavily on a well-established healthcare sector referral system. However, the referral system faces significant challenges in developing nations like Bangladesh. This study aimed to assess self-referral prevalence among patients attending tertiary care hospitals in Bangladesh and identify the associated factors.</p>
                <p style="text-align: justify;"><strong>Methods:</strong> This cross-sectional study was conducted at two tertiary care hospitals, involving 822 patients visiting their outpatient or inpatient departments. A semi-structured questionnaire was used for data collection. The patients' mode of referral (self-referral or institutional referral) was considered the outcome variable.</p>
                <p style="text-align: justify;"><strong>Results:</strong> Approximately 58% of the participants were unaware of the referral system. Of all, 59% (485 out of 822) of patients visiting tertiary care hospitals were self-referred, while 41% were referred by other healthcare facilities. The primary reasons for self-referral were inadequate treatment (28%), inadequate facilities (23%), critical cases (14%), and lack of expert physicians (8%). In contrast, institutional referrals were mainly attributed to inadequate facilities to treat the patient (53%), inadequate treatment (47%), difficult-to-treat cases (44%), and lack of expert physicians (31%) at the time of referral. Private facilities received a higher proportion of self-referred patients compared to government hospitals (68% vs. 56%, p < 0.001). Among patients attending the study sites through institutional referral, approximately 10% were referred from community clinics, 6% from union sub-centers, 25% from upazila health complexes, 22% from district hospitals, 22% from other tertiary care hospitals, and 42% from private clinics. Patients visiting the outpatient department (adjusted odds ratio [aOR] 3.3, 95% confidence interval [CI] 2.28-4.82, p < 0.001), residing in urban areas (aOR 1.29, 95% CI 1.04-1.64, p = 0.007), belonging to middle- and high-income families (aOR 1.34, 95% CI 1.03-1.62, p = 0.014, and aOR 1.98, 95% CI 1.54-2.46, p = 0.005, respectively), and living within 20 km of healthcare facilities (aOR 3.15, 95% CI 2.24-4.44, p < 0.001) exhibited a higher tendency for self-referral to tertiary care facilities.</p>
                <p style="text-align: justify;"><strong>Conclusions:</strong> A considerable number of patients in Bangladesh, particularly those from affluent urban areas and in proximity to healthcare facilities, tend to self-refer to tertiary care centers. Inadequacy of facilities in primary care centers significantly influences patients to opt for self-referral.</p>
